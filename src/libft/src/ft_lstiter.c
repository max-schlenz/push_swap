#include <libft.h>

void	ft_lstiter(t_list *lst, void (*f)(void *))
{
	t_list	*buf_list;

	if (!lst || !f)
		return ;
	while (lst)
	{
		buf_list = lst->next;
		f(lst->content);
		lst = buf_list;
	}
}
