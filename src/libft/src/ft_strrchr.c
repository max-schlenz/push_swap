#include <libft.h>

char	*ft_strrchr(const char *s, int c)
{
	int	i;

	i = ft_strlen(s);
	while (*(s + i) != (unsigned char)c)
	{
		if (i == 0)
			return (0);
		i--;
	}
	return ((char *)s + i);
}
