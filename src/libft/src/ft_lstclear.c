#include <libft.h>

void	ft_lstclear(t_list **lst, void (*del)(void*))
{
	t_list	*buf_list;

	if (!lst || !del)
		return ;
	while (*lst)
	{
		buf_list = (*lst)->next;
		ft_lstdelone(*lst, del);
		*lst = buf_list;
	}
}
